package com.yash.keyboard;

/**
 * @author Yash Harpale
 *
 */
public class MetaData {

	private int value;
	private String text;
	private int code;

	public void setValue(int value) {
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getText() {
		return text;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getCode() {
		return code;
	}

}
